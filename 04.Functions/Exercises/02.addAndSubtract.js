function main(num1, num2, num3) {

    let sumFirstAndSecond = sum(num1, num2);
    
    console.log(subtract(sumFirstAndSecond, num3));

    function sum(num1, num2) {
        return num1 + num2;
    }

    function subtract(sumFirstAndSecond, num3) {
        return sumFirstAndSecond - num3;
    }

}
