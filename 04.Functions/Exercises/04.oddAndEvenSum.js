function oddOrEvenSum(num1) {

    let oddSum = 0;
    let evenSum = 0;
    let numberAsString = num1.toString();

    for (let char of numberAsString) {
        let curentNumber = Number(char);
        if (curentNumber % 2 === 0) {
            evenSum += curentNumber;
        } else {
            oddSum += curentNumber;
        }  
    }

    console.log(`Odd sum = ${oddSum}, Even sum = ${evenSum}`);
    
}
